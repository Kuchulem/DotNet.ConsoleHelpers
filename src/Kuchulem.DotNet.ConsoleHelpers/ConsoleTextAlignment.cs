using System;
using System.Collections.Generic;
using System.Text;

namespace Kuchulem.DotNet.ConsoleHelpers
{
    /// <summary>
    /// List of alignments possible for console
    /// </summary>
    public enum ConsoleTextAlignment 
    { 
        /// <summary>
        /// Console text is align to console left
        /// </summary>
        Left = 0,
        /// <summary>
        /// Console text is align to console center
        /// </summary>
        Center = 1,
        /// <summary>
        /// Console text is align to console right
        /// </summary>
        Right = 2 
    };
}
